public class MessDatum {

    private int u;
    private double t,d;
    public MessDatum(int u, double t, double d){
        this.u = u;
        this.t = t;
        this.d = d;
    }

    public int getU() {
        return u;
    }
    public double getT() {
        return t;
    }
    public double getD() {
        return d;
    }
}
